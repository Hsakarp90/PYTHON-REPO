def add_three_numbers(a, b, c):
    return a + b + c

# Example usage:
num1 = int(input("Enter first number: "))
num2 = int(input("Enter second number: "))
num3 = int(input("Enter third number: "))

sum_result = add_three_numbers(num1, num2, num3)
print("The sum is:", sum_result)
